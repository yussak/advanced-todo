import Head from "next/head";
import { Inter } from "next/font/google";
import { useEffect, useState } from "react";
import Link from "next/link";
import { SubmitHandler, useForm } from "react-hook-form";
import { api } from "@/lib/api-client";

const inter = Inter({ subsets: ["latin"] });
export default function Home() {
  const [todos, setTodos] = useState([]);
  // 削除時のフラッシュ
  const [flashMessage, setFlashMessage] = useState<string | null>(null);

  // TODO:ここもawaitにすべきかもしれないが単純につけるだけだとだめそうので調べる
  useEffect(() => {
    fetchTodos();
  }, []);

  const fetchTodos = async () => {
    try {
      const res = await api.get("/todos");
      setTodos(res.data);
    } catch (error) {
      console.error(error);
    }
  };

  const addTodo = async (data: Inputs) => {
    const { title, body } = data;
    try {
      await api.post("/todo", {
        title,
        body,
      });
    } catch (error) {
      console.error(error);
    }
    await fetchTodos();
    reset();
  };

  type Inputs = {
    title: string;
    body: string;
  };

  const handleDelete = async (id: string) => {
    try {
      await api.delete(`/todo/${id}`);
      setFlashMessage("Todo deleted");
      setTimeout(() => setFlashMessage(null), 3000);
    } catch (error) {
      console.error(error);
    }
    await fetchTodos();
  };

  const {
    register,
    handleSubmit,
    formState: { errors },
    reset,
  } = useForm<Inputs>();
  const onSubmit: SubmitHandler<Inputs> = async (data) => await addTodo(data);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="bg-gray-200">
        {/* TODO:出る時ずれるので修正 */}
        {flashMessage && (
          <div
            className="bg-green-100 border border-green-400 text-green-700 px-4 py-3 rounded relative"
            role="alert"
          >
            <strong className="font-bold">{flashMessage}</strong>
          </div>
        )}

        <h1 className="text-3xl font-bold underline">TodoList</h1>
        <form onSubmit={handleSubmit(onSubmit)}>
          <label htmlFor="title" className="text-red-600">
            Title
          </label>
          <input
            {...register("title", {
              required: "this field is required.",
            })}
          />
          {errors.title && <span>{errors.title.message}</span>}
          <label htmlFor="body">Body</label>
          <input
            {...register("body", { required: "this field is required." })}
          />
          {errors.body && <span>{errors.body.message}</span>}
          <input type="submit" value="add" />
        </form>
        {todos.map((todo) => (
          <p key={todo.id}>
            {todo.title}, {todo.body}
            <button onClick={() => handleDelete(todo.id)}>delete</button>
            <Link href={`/todos/${todo.id}`}>detail</Link>
          </p>
        ))}
      </main>
    </>
  );
}
